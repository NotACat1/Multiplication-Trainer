@Immutable
data class ColorFamily(
    val color: Color,           // Основной цвет
    val onColor: Color,         // Цвет элементов на основном цвете
    val colorContainer: Color,  // Цвет контейнера
    val onColorContainer: Color // Цвет текста/иконок на контейнере
)

