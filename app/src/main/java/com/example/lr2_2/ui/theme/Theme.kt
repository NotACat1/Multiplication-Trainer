/**
 * Применяет Material 3 тему ко всему UI.
 *
 * @param darkTheme — использовать ли тёмную тему (по умолчанию определяется системой)
 * @param dynamicColor — использовать ли динамическую палитру (Android 12+)
 * @param content — содержимое, к которому применяется тема
 */
@Composable
fun AppTheme(
    darkTheme: Boolean = isSystemInDarkTheme(),
    // Динамическая цветовая схема доступна только на Android 12 и выше
    dynamicColor: Boolean = true,
    content: @Composable () -> Unit
) {
    // Определяем используемую схему
    val colorScheme = when {
        // Если включена динамическая цветовая схема и ОС поддерживает
        dynamicColor && Build.VERSION.SDK_INT >= Build.VERSION_CODES.S -> {
            val context = LocalContext.current
            if (darkTheme) dynamicDarkColorScheme(context) else dynamicLightColorScheme(context)
        }
        // Иначе выбираем базовую схему в зависимости от темы
        darkTheme -> darkScheme
        else -> lightScheme
    }

    // Применяем MaterialTheme со стилем приложения
    MaterialTheme(
        colorScheme = colorScheme,
        typography = AppTypography,
        content = content
    )
}
